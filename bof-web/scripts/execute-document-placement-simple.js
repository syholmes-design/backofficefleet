async function processDriver(driverId) {
  console.log(`\n🔍 Processing driver ${driverId}...`);
  
  const sourceDir = path.join(process.cwd(), 'public', 'documents', 'drivers', driverId);
  const targetDir = path.join(process.cwd(), 'public', 'generated', 'drivers', driverId);
  
  if (!fs.existsSync(sourceDir)) {
    console.log(`❌ Source directory not found: ${sourceDir}`);
    return { success: false, message: 'Source directory not found' };
  }
  
  // Ensure target directory exists
  fs.mkdirSync(targetDir, { recursive: true });
  
  const sourceFiles = fs.readdirSync(sourceDir);
  const processedFiles = [];
  const missingDocuments = [];
  const duplicates = [];
  
  // Process existing files
  for (const fileName of sourceFiles) {
    const sourcePath = path.join(sourceDir, fileName);
    const stats = fs.statSync(sourcePath);
    const category = categorizeFile(fileName);
    const quality = assessFileQuality(fileName, stats.size);
    
    const targetFileName = fileName.replace(/\.[^/.]+$/, '.html');
    const targetPath = path.join(targetDir, targetFileName);
    
    processedFiles.push({
      fileName,
      sourcePath,
      targetPath,
      category: category.category,
      documentType: category.type,
      priority: category.priority,
      quality,
      size: stats.size
    });
    
    // Move/copy file to generated directory
    try {
      if (fileName.endsWith('.html')) {
        fs.copyFileSync(sourcePath, targetPath);
      } else if (fileName.endsWith('.png')) {
        createImageHtmlWrapper(sourcePath, targetPath, category.type, fileName);
      }
      console.log(`  ✅ ${fileName} → ${category.category} (${quality})`);
    } catch (error) {
      console.log(`  ❌ Failed to process ${fileName}: ${error.message}`);
    }
  }
  
  // Check what's missing
  const requiredCategories = ['CDL', 'Medical Certification', 'MVR', 'Driver Application', 'I-9', 'W-9'];
  const foundCategories = new Set(processedFiles.map(f => f.category));
  
  for (const required of requiredCategories) {
    if (!foundCategories.has(required)) {
      missingDocuments.push(required);
      const placeholderPath = path.join(targetDir, `${required.toLowerCase().replace(/\s+/g, '_')}.html`);
      createMissingDocumentPlaceholder(placeholderPath, required, `${required.toLowerCase().replace(/\s+/g, '_')}.html`);
      console.log(`  🚫 ${required} - Missing (placeholder created)`);
    }
  }
  
  return {
    success: true,
    driverId,
    processedFiles: processedFiles.length,
    missingDocuments: missingDocuments.length,
    duplicates: duplicates.length,
    categories: foundCategories.size
  };
}

async function main() {
  console.log('🚀 Starting BOF Vault Document Placement Process');
  console.log('================================================');
  
  const driverIds = [
    'DRV-001', 'DRV-002', 'DRV-003', 'DRV-004', 'DRV-005',
    'DRV-006', 'DRV-007', 'DRV-008', 'DRV-009', 'DRV-010',
    'DRV-011', 'DRV-012'
  ];
  
  const results = {
    totalDrivers: driverIds.length,
    successfulDrivers: 0,
    totalFilesProcessed: 0,
    totalMissingDocuments: 0,
    errors: []
  };
  
  for (const driverId of driverIds) {
    try {
      const result = await processDriver(driverId);
      if (result.success) {
        results.successfulDrivers++;
        results.totalFilesProcessed += result.processedFiles;
        results.totalMissingDocuments += result.missingDocuments;
      } else {
        results.errors.push(`${driverId}: ${result.message}`);
      }
    } catch (error) {
      results.errors.push(`${driverId}: ${error.message}`);
    }
  }
  
  console.log('\n📊 BOF Vault Document Placement Summary');
  console.log('================================================');
  console.log(`Total Drivers Processed: ${results.successfulDrivers}/${results.totalDrivers}`);
  console.log(`Total Files Processed: ${results.totalFilesProcessed}`);
  console.log(`Total Missing Documents: ${results.totalMissingDocuments}`);
  console.log(`Errors: ${results.errors.length}`);
  
  if (results.errors.length > 0) {
    console.log('\n❌ Errors encountered:');
    results.errors.forEach(error => console.log(`  - ${error}`));
    process.exit(1);
  }
  
  console.log('\n✅ Document placement completed successfully!');
  console.log('\nNext Steps:');
  console.log('- Review generated documents in /public/generated/drivers/');
  console.log('- Upload missing documents through driver onboarding');
  console.log('- Test ComplianceFlow Pro integration');
  console.log('- Verify all documents appear in correct BOF Vault categories');
  
  process.exit(0);
}

main().catch(error => {
  console.error('\n❌ Document placement failed:', error);
  process.exit(1);
});
