/**
 * Simple Document Placement Script
 * 
 * Directly organizes existing driver documents into BOF Vault structure
 * without TypeScript compilation dependencies.
 */

const fs = require('fs');
const path = require('path');

// Document type mapping
const DOCUMENT_CATEGORIES = {
  'cdl': { type: 'CDL', category: 'CDL', priority: 'critical' },
  'medical': { type: 'Medical Certification', category: 'Medical Certification', priority: 'critical' },
  'medcard': { type: 'Medical Certification', category: 'Medical Certification', priority: 'critical' },
  'mvr': { type: 'MVR', category: 'MVR', priority: 'critical' },
  'application': { type: 'Driver Application', category: 'Driver Application', priority: 'high' },
  'i9': { type: 'I-9', category: 'Employment / I-9', priority: 'high' },
  'w9': { type: 'W-9', category: 'W-9', priority: 'high' },
  'fmcsa': { type: 'FMCSA Clearinghouse', category: 'FMCSA / Compliance', priority: 'medium' },
  'emergency': { type: 'Emergency Contact', category: 'Emergency Contact', priority: 'medium' },
  'insurance': { type: 'Road Test Certificate', category: 'Other / Supporting Docs', priority: 'low' },
  'qualification': { type: 'Road Test Certificate', category: 'Other / Supporting Docs', priority: 'low' }
};

function categorizeFile(fileName) {
  const lower = fileName.toLowerCase();
  
  for (const [key, mapping] of Object.entries(DOCUMENT_CATEGORIES)) {
    if (lower.includes(key)) {
      return mapping;
    }
  }
  
  return { type: 'Other / Supporting Docs', category: 'Other / Supporting Docs', priority: 'low' };
}

function assessFileQuality(fileName, fileSize) {
  if (fileName.includes('cdl') && fileName.endsWith('.png')) {
    if (fileSize < 500000) return 'poor'; // < 500KB
    if (fileSize < 1000000) return 'fair'; // < 1MB
    return 'good'; // >= 1MB
  }
  
  if (fileName.endsWith('.html')) {
    if (fileSize < 10000) return 'poor'; // < 10KB
    if (fileSize < 20000) return 'fair'; // < 20KB
    return 'good'; // >= 20KB
  }
  
  return 'fair';
}

